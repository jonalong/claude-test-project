const app = express();
app.use(cors({origin: true}));
app.use(express.json());

app.post("/api/purify", async (req, res) => {
  const {text, tone = "정중하게"} = req.body;

  if (!text?.trim()) {
    return res.status(400).json({error: "텍스트를 입력해주세요."});
  }

  const toneDesc = TONE_PROMPT[tone] ??
    "말투: 정중하고 격식 있는 어체로 순화할 것";
  const dynamicRule = toneDesc;

  try {
    const anthropic = new Anthropic({apiKey: anthropicApiKey.value()});
    const message = await anthropic.messages.create({
      model: "claude-haiku-4-5-20251001",
      max_tokens: 1024,
      system: [
        {
          type: "text",
          text: STABLE_SYSTEM_PROMPT,
          cache_control: {type: "ephemeral"},
        },
        {
          type: "text",
          text: dynamicRule,
        },
      ],
      messages: [{role: "user", content: `<INPUT>${text}</INPUT>`}],
    });

    const usage = message.usage;
    console.log(
        `[Cache] read=${usage.cache_read_input_tokens ?? 0}` +
        ` write=${usage.cache_creation_input_tokens ?? 0}` +
        ` input=${usage.input_tokens} output=${usage.output_tokens}`,
    );

    const raw = message.content[0].text.trim();
    let parsed;

    try {
      // eslint-disable-next-line max-len
      const jsonMatch = raw.match(/```json\s*([\s\S]*?)\s*```/) || raw.match(/({[\s\S]*})/);
      parsed = JSON.parse(jsonMatch ? jsonMatch[1] : raw);
    } catch {
      parsed = {
        refined: raw,
        scoreBefore: 50,
        scoreAfter: 10,
        level: "욱! 😠",
        comment: "변환이 완료되었어요.",
      };
    }

    if (!parsed.refined && parsed.result) parsed.refined = parsed.result;
    res.json(parsed);
  } catch (err) {
    console.error("Claude API error:", err.message);
    res.status(500).json({error: "변환에 실패했습니다."});
  }
});

exports.api = onRequest({secrets: [anthropicApiKey], invoker: "public"}, app);
